        // テキスト用のバウンディングボックスを作成
        $day = $i + 1 + $j * 7 - $top_weekday;
        if ($day <= 0){
            $day = " ";
        } elseif ($day > $last_day){
            $day = " ";
        }
        $is_holiday = false;
        if ($day !== " ") {
            if (in_array(date("Y/m/d", mktime(0, 0, 0, $month, $day, $year)), $holiday)) {
                $is_holiday = true;
            }
        }
        $bbox = imagettfbbox($font_size, $angle, $font, $day);
        $tlx = $margin + $i * $cell_width;
        $tly = $margin + ($j + 1) * $cell_height;
