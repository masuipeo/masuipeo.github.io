    // テキスト用のバウンディングボックスを作成
    $bbox = imagettfbbox($font_size - 20, $angle, $font, $title[$i]);
    $tlx = $margin + $i * $cell_width;
    $tly = $margin;
    $x = $tlx + ($cell_width / 2) - ($bbox[4] / 2);
    $y = $tly + ($cell_height / 2) - ($bbox[5] / 2);
    imagettftext(
        $img,
        $font_size - 20,
        $angle,
        $x,
        $y,
        ($i == 0)?$red:(($i == 6)?$blue:$black),
        $font,
        $title[$i]
    );
